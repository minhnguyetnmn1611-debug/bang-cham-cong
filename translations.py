def get_t(lang):
    def t(key, *args, **kwargs):
        text = TRANSLATIONS.get(lang, TRANSLATIONS["vi"]).get(key, key)
        if isinstance(text, str) and ("\\u" in text or "\\U" in text):
            try:
                res = text.encode('utf-8').decode('unicode_escape')
                try:
                    res = res.encode('latin-1').decode('utf-8')
                except Exception:
                    pass
                text = res
            except Exception:
                pass
        if args or kwargs:
            try:
                return text.format(*args, **kwargs)
            except (KeyError, IndexError):
                return text
        return text
    return t

def get_data_t(lang):
    lang_dict = DATA_TRANSLATIONS.get(lang, DATA_TRANSLATIONS["vi"])
    # Build lowercase lookup for case-insensitive matching
    lower_dict = {k.lower(): v for k, v in lang_dict.items()}
    def t_data(val):
        if not val or not isinstance(val, str):
            return val
        # Exact match first
        if val in lang_dict:
            return lang_dict[val]
        # Case-insensitive fallback
        return lower_dict.get(val.lower(), val)
    return t_data


